def get_overall_template_hi(average_score: float) -> str:
    """Return the Hindi overall summary text based on average score across all areas."""
    from templates.horoscope_templates import score_to_band
    band = score_to_band(average_score)
    return OVERALL_TEMPLATES_HI.get(band, "आज ग्रहीय ऊर्जाओं का संतुलन बना हुआ है।")
